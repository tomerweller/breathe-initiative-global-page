<h3>Hackathon for respiratory patients.</h3>

<p>Imperial College Advanced Hackspace (ICAH) presents its largest hackathon yet. A global event which will take place simultaneously across three continents - at ICAH in London, UK; MIT Media Lab in Boston, USA and at Technion – Israel Institute of Technology in Haifa, Israel.</p>

<p>Dubbed the BREATHE Respiratory Hackathons, these events will bring together designers, technicians, engineers, patients, doctors and creative thinking entrepreneurs and innovators to develop synergistic innovations and devices with patient-centered wellness and treatment as the focus.</p>

<p>Teams will be given free rein to explore solutions to many of the problems respiratory sufferers face daily. That may include integrating multiple devices for nighttime and morning use, making existing equipment lighter weight, adding Bluetooth interfaces to equipment to allow control through a smartphone, or any number of ideas that push the status quo and challenge the respiratory industry to innovate around patient-centered wellness.</p>

<p>Results from all three Hackathons will be shared with the media and 22,000+ attendees at the <a style="color: #3399FF" href="http://www.erscongress.org/">European Respiratory Society Conference</a> in Amsterdam September 27, 2015.</p>
